// pure-function adalah fungsi yang tidak bergantung terhadap nilai yang berada diluar fungsi atau parameternya
const hitungLuasLingkaran = (jariJari) => {
    return 3.14 * (jariJari * jariJari);
}

console.log(hitungLuasLingkaran(4));
console.log(hitungLuasLingkaran(12.9));
console.log(hitungLuasLingkaran(198));
